export const updateFollower: RequestHandler = async (req, res) => {
  const { profileId } = req.params;
  let status: "followed" | "unfollowed";

  if (!isValidObjectId(profileId)) {
    return res.status(400).send("Invalid profileId");
  }

  const profile = await User.findById(profileId);
  if (!profile) {
    return res.status(404).send("Profile not found");
  }

  const alreadyAFollower = await User.findOne({
    _id: profileId,
    followers: req.user.id,
  });
  if (alreadyAFollower) {
    await User.updateOne(
      { _id: profileId },
      { $pull: { followers: req.user.id } }
    );
    status = "unfollowed";
  } else {
    await User.updateOne(
      { _id: profileId },
      { $addToSet: { followers: req.user.id } }
    );
    status = "followed";
  }
  if (status === "followed") {
    await User.updateOne(
      { _id: req.user.id },
      { $addToSet: { followings: profileId } }
    );
  } else {
    await User.updateOne(
      { _id: req.user.id },
      { $pull: { followings: profileId } }
    );
  }
  res.json({ status });
};

export const getUploads: RequestHandler = async (req, res) => {
  const { pageNo = "0", limit = "20" } = req.query as paginationQuery;

  const data = await Audio.find({ owner: req.user.id })
    .skip(parseInt(pageNo) * parseInt(limit))
    .limit(parseInt(limit))
    .sort("-createdAt");

  const audios = data.map((audio) => {
    return {
      id: audio._id,
      title: audio.title,
      about: audio.about,
      file: audio.file.url,
      category: audio.category,
      poster: audio.poster?.url,
      date: audio.createdAt,
      owner: {
        name: req.user.name,
        id: req.user.id,
      },
    };
  });

  res.json({ audios });
};

